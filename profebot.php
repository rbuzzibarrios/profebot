// ── VERSION ──
function pb_version()
{
    static $v = null;
    if ($v !== null) {
        return $v;
    }
    $f = __DIR__.'/VERSION';
    if (is_file($f)) {
        $v = trim(file_get_contents($f));
        if ($v !== '') {
            return $v;
        }
    }
    $v = 'dev';
    return $v;
}

// ── LOGGER ──
function pb_log()
{
    static $logger = null;
    if ($logger !== null) {
        return $logger;
    }
    if (!class_exists('Monolog\\Logger')) {
        $logger = false; // marker: Monolog not available
        return $logger;
    }
    $logger = new Logger('profebot');
    $logger->pushHandler(new StreamHandler('php://stderr', Logger::DEBUG));
    return $logger;
}

function pb_log_warn($msg, array $ctx = [])
{
    $l = pb_log();
    if ($l) {
        $l->warning($msg, $ctx);
    } else {
        error_log('[WARN] '.$msg.' '.json_encode($ctx, JSON_UNESCAPED_UNICODE));
    }
}

function pb_log_error($msg, array $ctx = [])
{
    $l = pb_log();
    if ($l) {
        $l->error($msg, $ctx);
    } else {
        error_log('[ERROR] '.$msg.' '.json_encode($ctx, JSON_UNESCAPED_UNICODE));
    }
}

function pb_log_info($msg, array $ctx = [])
{
    $l = pb_log();
    if ($l) {
        $l->info($msg, $ctx);
    } else {
        error_log('[INFO] '.$msg.' '.json_encode($ctx, JSON_UNESCAPED_UNICODE));
    }
}

