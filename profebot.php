                        $text = $d['content'][0]['text'] ?? null;
                        $err = $d['error']['message'] ?? null;
                        $usage = $d['usage'] ?? null;
                        return ['text' => $text, 'error' => $err, 'usage' => $usage];
                    },
            ],
            'gemini' => [
                    'env'   => 'GEMINI_API_KEY',
                    'build' => function ($data, $key) {
                        $parts = [];
                        if (!empty($data['system'])) {
                            $parts[] = ['text' => $data['system']];
                        }
                        if (!empty($data['messages'])) {
                            foreach ($data['messages'] as $m) {
                                $parts[] = ['text' => $m['content']];
                            }
                        }
                        $body = [
                                'contents'         => [['parts' => $parts]],
                                'generationConfig' => ['maxOutputTokens' => 2048],
                        ];
                        if (!empty($data['system'])) {
                            $body['systemInstruction'] = ['parts' => [['text' => $data['system']]]];
                            $body['contents'] = [
                                    [
                                            'parts' => array_values(array_filter($parts, function ($p) use ($data) {
                                                return $p['text'] !== $data['system'];
                                            })),
                                    ],
                            ];
                        }
                        return [
                                'url'     => 'https://generativelanguage.googleapis.com/v1beta/models/gemini-2.5-flash:generateContent?key='.$key,
                                'headers' => ['Content-Type: application/json'],
                                'body'    => json_encode($body),
                        ];
                    },
                    'parse' => function ($resp) {
                        $d = json_decode($resp, true);
                        $text = $d['candidates'][0]['content']['parts'][0]['text'] ?? null;
                        $err = $d['error']['message'] ?? null;
                        $usage = $d['usageMetadata'] ?? null;
                        return ['text' => $text, 'error' => $err, 'usage' => $usage];
                    },
            ],
    ];

    // Read keys and priority order from JSON body
    $clientKeys = isset($data['providers']) ? $data['providers'] : [];
    $order = isset($data['provider_order']) ? $data['provider_order'] : array_keys($PROVIDERS);

    // Resolve keys: client-sent > env var; filter providers without key
    $resolved = [];
    foreach ($order as $pid) {
        if (!isset($PROVIDERS[$pid])) {
            continue;
        }
        $k = '';
        if (!empty($clientKeys[$pid])) {
            $k = $clientKeys[$pid];
        } elseif (!empty($CONFIG_KEYS[$pid])) {
            $k = $CONFIG_KEYS[$pid];
        } elseif (getenv($PROVIDERS[$pid]['env'])) {
            $k = getenv($PROVIDERS[$pid]['env']);
        }
        if ($k !== '') {
            $resolved[$pid] = $k;
        }
    }

    if (empty($resolved)) {
        header('Content-Type: application/json');
        http_response_code(401);
        echo json_encode(['error' => 'No hay proveedores configurados. Agregá al menos una API key.']);
        exit;
    }

    // Inject curriculum materials context (server-side)
    $materials_chars = 0;
    $images_chars = 0;
    if (!empty($data['material_grade']) && !empty($data['material_subj'])) {
        $unit_idx = isset($data['material_unit_idx']) ? $data['material_unit_idx'] : null;
        $extra = build_default_ctx($data['material_grade'], $data['material_subj'], $unit_idx, $DEFAULT_MATERIALS);
        if ($extra) {
            $data['system'] = (isset($data['system']) ? $data['system'] : '').$extra;
            $materials_chars = strlen($extra);
        }
        $imgCtx = build_image_ctx($data['material_grade'], $data['material_subj'], $unit_idx);
        if ($imgCtx) {
            $data['system'] = (isset($data['system']) ? $data['system'] : '').$imgCtx;
            $images_chars = strlen($imgCtx);
        }
    }

    // Capture cache fallback info before stripping control fields
    $fallbackCacheKey = isset($data['cache_key']) ? $data['cache_key'] : '';
    $fallbackExclude = isset($data['cache_exclude']) && is_array($data['cache_exclude']) ? $data['cache_exclude'] : [];

    // Debug-only: simulate error scenarios without breaking real providers.
    // Enable with env var PROFEBOT_DEBUG=1 + body JSON: {"_simulate":"ai_busy_no_cache"|"ai_busy_with_cache"}
    $simulate = (getenv('PROFEBOT_DEBUG') === '1' && !empty($data['_simulate'])) ? $data['_simulate'] : '';

    // Strip control fields from body before sending to provider
    unset($data['providers'], $data['provider_order'], $data['material_grade'], $data['material_subj'], $data['material_unit_idx'], $data['cache_key'], $data['cache_exclude'], $data['_simulate']);

    // Fallback loop
    $errors = [];
    $stopError = null; // remembers a config error (400/401) to surface if nothing else works
    $STOP_CODES = [400, 401]; // config errors, do not retry the SAME provider
    $FALLBACK_CODES = [429, 402, 403, 500, 502, 503];

    header('Content-Type: application/json');
    header('Access-Control-Allow-Origin: *');

    if ($simulate === 'ai_busy_no_cache') {
        pb_log_warn('Simulating ai_busy_no_cache (forcing empty cache fallback)');
        $errors[] = 'simulated: all providers busy';
        $fallbackCacheKey = '__simulate_no_match__'; // guarantees cache miss
        goto cache_fallback;
    }
    if ($simulate === 'ai_busy_with_cache') {
        pb_log_warn('Simulating ai_busy_with_cache (forcing cache fallback path)');
        $errors[] = 'simulated: all providers busy';
        goto cache_fallback;
    }

    foreach ($resolved as $pid => $key) {
        $prov = $PROVIDERS[$pid];
        $req = $prov['build']($data, $key);

        $ch = curl_init($req['url']);
        curl_setopt_array($ch, [
                CURLOPT_RETURNTRANSFER => true,
                CURLOPT_POST           => true,
                CURLOPT_POSTFIELDS     => $req['body'],
                CURLOPT_HTTPHEADER     => $req['headers'],
                CURLOPT_TIMEOUT        => 60,
                CURLOPT_SSL_VERIFYPEER => false,
                CURLOPT_SSL_VERIFYHOST => 0,
        ]);

        $response = curl_exec($ch);
        $httpCode = curl_getinfo($ch, CURLINFO_HTTP_CODE);
        $curlErr = curl_error($ch);
        curl_close($ch);

        // Error cURL → fallback
        if ($curlErr) {
            $errors[] = "$pid: red – $curlErr";
            pb_log_warn('Provider network error', ['provider' => $pid, 'curl_error' => $curlErr]);
            continue;
        }

        // HTTP error → decide whether to stop or fallback
        if ($httpCode >= 400) {
            $parsed = $prov['parse']($response);
            $msg = $parsed['error'] ?: "HTTP $httpCode";
            $errors[] = "$pid: $msg";
            if (in_array($httpCode, $STOP_CODES)) {
                // Config error (bad key, unsupported request). Don't retry this provider,
                // but keep trying the rest of the chain — another provider may still work.
                // E.g. Gemini geo-block returns 400 ("User location is not supported") on
                // Render even though Groq is fine. Remember it to surface a precise message
                // only if every provider (and the cache) fails.
                $stopError = ['code' => $httpCode, 'msg' => "$pid: $msg", 'provider' => $pid];
                pb_log_warn('Provider config error (skip, try next)', [
                    'provider' => $pid,
                    'http_code' => $httpCode,
                    'message' => $msg,
                    'raw' => substr((string)$response, 0, 1000),
                ]);
                continue;
            }
            pb_log_warn('Provider HTTP error (fallback)', [
                'provider' => $pid,
                'http_code' => $httpCode,
                'message' => $msg,
                'raw' => substr((string)$response, 0, 1000),
            ]);
            continue; // fallback
        }

        // Success → parse and return
        $parsed = $prov['parse']($response);
        if ($parsed['text']) {
            $userChars = 0;
            foreach (($data['messages'] ?? []) as $m) {
                $userChars += strlen($m['content'] ?? '');
            }
            $out = [
                'content'  => [['text' => $parsed['text']]],
                'provider' => $pid,
                'stats'    => [
                    'system_chars'    => strlen($data['system'] ?? ''),
                    'user_chars'      => $userChars,
                    'materials_chars' => $materials_chars,
                    'images_chars'    => $images_chars,
                ],
            ];
            if (!empty($parsed['usage'])) {
                $out['usage'] = $parsed['usage'];
            }
            echo json_encode($out, JSON_UNESCAPED_UNICODE);
            exit;
        }
        $errors[] = "$pid: respuesta vacía";
        pb_log_warn('Provider empty response', ['provider' => $pid]);
    }

    // All providers failed → last resort: cache (progressive widening search)
    cache_fallback:
    pb_log_error('All providers failed', ['errors' => $errors, 'cache_key' => $fallbackCacheKey]);

    $cacheHit = null;
    $cacheMatchType = '';
    if ($fallbackCacheKey) {
        $excludeFn = function ($q) use ($fallbackExclude) {
            return !in_array($q['question'], $fallbackExclude);
        };

        // 1) Exact cache_key match
        $exactItems = cache_get_key($fallbackCacheKey);
        if (!empty($exactItems)) {
            $pool = array_values(array_filter($exactItems, $excludeFn));
            if (!empty($pool)) {
                $cacheHit = $pool[array_rand($pool)];
                $cacheMatchType = 'exact';
            }
        }

        // 2) Widening: same grade::subj::idx (any difficulty)
        if (!$cacheHit) {
            $parts = explode('::', $fallbackCacheKey);
            // Last component is difficulty → drop it
            if (count($parts) >= 2) {
                $prefixWithoutDiff = implode('::', array_slice($parts, 0, count($parts) - 1)).'::';
                $matches = cache_scan_prefix($prefixWithoutDiff);
                $pool = [];
                foreach ($matches as $items) {
                    $pool = array_merge($pool, array_filter($items, $excludeFn));
                }
                $pool = array_values($pool);
                if (!empty($pool)) {
                    $cacheHit = $pool[array_rand($pool)];
                    $cacheMatchType = 'same_objective';
                }
            }
        }

        // 3) Widening: same grade::subj (any objective and difficulty)
        if (!$cacheHit) {
            $parts = explode('::', $fallbackCacheKey);
            // grade::subj is the first 2 parts in current format (1ro::mat::U::O::diff) or just the first in legacy (mat::U::O::diff)
            $prefixSubj = (count($parts) >= 4 ? $parts[0].'::'.$parts[1] : $parts[0]).'::';
            $matches = cache_scan_prefix($prefixSubj);
            $pool = [];
            foreach ($matches as $items) {
                $pool = array_merge($pool, array_filter($items, $excludeFn));
            }
            $pool = array_values($pool);
            if (!empty($pool)) {
                $cacheHit = $pool[array_rand($pool)];
                $cacheMatchType = 'same_subject';
            }
        }
    }

    if ($cacheHit) {
        pb_log_info('Cache fallback served', ['cache_key' => $fallbackCacheKey, 'match' => $cacheMatchType]);
        $warningKid = '¡Hoy juego con preguntas guardadas! Mi cerebro mágico está descansando.';
        $warning = 'El servicio de IA está saturado. Usando una pregunta del caché.';
        echo json_encode([
            'cached' => true,
            'question' => $cacheHit,
            'provider' => 'cache_fallback',
            'warning' => $warning,
            'warning_kid' => $warningKid,
        ], JSON_UNESCAPED_UNICODE);
        exit;
    }

    pb_log_error('Cache fallback empty', ['cache_key' => $fallbackCacheKey]);
    // If a provider hit a config error (e.g. bad key with a single provider) and nothing
    // else worked, surface that precise message instead of the generic "AI busy" one.
    if ($stopError) {
        http_response_code($stopError['code']);
        echo json_encode(['error' => $stopError['msg'], 'provider' => $stopError['provider']]);
        exit;
    }
    http_response_code(502);
    echo json_encode([
        'code' => 'AI_BUSY_NO_CACHE',
        'error' => 'El servicio de IA está ocupado y todavía no hay preguntas guardadas para este tema. Tocá Reintentar.',
        'error_kid' => '¡Ay! Mi cerebro mágico está pensando muy fuerte. Vamos a tocar el botón grande y probamos otra vez juntos.',
    ]);
    exit;
}

